public class NestedIfStatement {
    public static void main(String[] args) {
        int a=20;
        int b=10;
        if(a==20){
            if(b==10){
                System.out.println("Web Automation Tester");
            }
        }
        else {
            System.out.println("SoftWare Developer");
        }
    }
}
