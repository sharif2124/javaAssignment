package Class_3.loop;

public class ForEachLoop {
    public static void main(String[] args) {
        int []a={10,20,30,40};
        for(int i :a){
            System.out.println(i);
        }
 //For String Example
        String []str={"Bangladesh","India","USA","UK","Canada"};
        for(String s:str){
            System.out.println(s);
        }
    }
}
