package Class_2;

public class Arithmetic_Operator {
    public static void main(String[] args) {
        int num1 = 40;
        int num2 = 20;

        System.out.println(num1+num2);//60
        System.out.println(num1-num2);//20
        System.out.println(num1/num2);//2
        System.out.println(num1%num2);//0
    }
}
